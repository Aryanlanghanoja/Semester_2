    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:

        if(VeicleClass.getSelectedItem().toString().equals("") ||  Model.getSelectedItem().toString().equals("")||  Color.getSelectedItem().toString().equals("") ||  Type.getSelectedItem().toString().equals("") ||  Engine.getSelectedItem().toString().equals("") ||  BreakingSystem.getSelectedItem().toString().equals("") || EngineCapacity.getText().equals("") || BatteryCapacity.getText().equals("") )
        {
            JOptionPane.showMessageDialog(this, "Please Enter All The Required Information!");
        }

        else
        {
            String data[] = {VeicleClass.getSelectedItem().toString(),Model.getSelectedItem().toString(),Color.getSelectedItem().toString(),Type.getSelectedItem().toString(),Engine.getSelectedItem().toString(),BreakingSystem.getSelectedItem().toString(),EngineCapacity.getText(),BatteryCapacity.getText()};

            DefaultTableModel tb1Model = (DefaultTableModel)jTable1.getModel();
            tb1Model.addColumn("Parameters");
            tb1Model.addColumn("Choices");
            tb1Model.addRow(new Object[]{"Vehicle Class", data[0]});
            tb1Model.addRow(new Object[]{"Model", data[1]});
            tb1Model.addRow(new Object[]{"Color", data[2]});
            tb1Model.addRow(new Object[]{"Type", data[3]});
            tb1Model.addRow(new Object[]{"Engine", data[4]});
            tb1Model.addRow(new Object[]{"Breaking System", data[5]});
            tb1Model.addRow(new Object[]{"Engine Capacity", data[6]});
            tb1Model.addRow(new Object[]{"Battery Capacity", data[7]});

            //tb1Model.addRow(new Object[]{"Jane Smith", 25});
            JOptionPane.showMessageDialog(this, "Choices Added Sucessfully!");
            EngineCapacity.setText("");
            BatteryCapacity.setText(" ");
            btnOk.setEnabled(false);
        }

    }//GEN-LAST:event_btnOkActionPerformed

